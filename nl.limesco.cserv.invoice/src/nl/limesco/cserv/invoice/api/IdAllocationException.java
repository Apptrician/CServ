package nl.limesco.cserv.invoice.api;

public class IdAllocationException extends Exception {

	public IdAllocationException() {
		super();
	}

	public IdAllocationException(String message, Throwable cause) {
		super(message, cause);
	}

	public IdAllocationException(String message) {
		super(message);
	}

	public IdAllocationException(Throwable cause) {
		super(cause);
	}

}
