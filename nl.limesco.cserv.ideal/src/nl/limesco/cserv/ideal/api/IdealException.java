package nl.limesco.cserv.ideal.api;

public class IdealException extends Exception {

	public IdealException() {
		super();
	}

	public IdealException(String message, Throwable cause) {
		super(message, cause);
	}

	public IdealException(String message) {
		super(message);
	}

	public IdealException(Throwable cause) {
		super(cause);
	}

}
